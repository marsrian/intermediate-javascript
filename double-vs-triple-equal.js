// double equal : value same 
const first = 2;
const second = "2";
if (first == second) {
    console.log("condition is true");
} else {
    console.log("condition is false");
}
// double equal :  1 and true value same
const age = 1;
const age2 = true;
if (age == age2) {
    console.log("condition is correct");
} else {
    console.log("condition is not correct");
}

